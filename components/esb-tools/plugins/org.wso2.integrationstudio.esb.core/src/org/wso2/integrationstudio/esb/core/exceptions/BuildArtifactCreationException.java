package org.wso2.integrationstudio.esb.core.exceptions;

public class BuildArtifactCreationException extends Exception {
    
    public BuildArtifactCreationException() {
        super();
    }
    
    public BuildArtifactCreationException(String message) {
        super(message);
    }

    public BuildArtifactCreationException(String message, Throwable cause) {
        super(message, cause);
    }

    public BuildArtifactCreationException(Throwable cause) {
        super(cause);
    }

}
